/**
 * <p>
 * Integration test to ensure that the Project Collaborators Page.
 * </p>
 * 
 * @author Josh Adam <josh.adam@phac-aspc.gc.ca>
 */
@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(loader = AnnotationConfigContextLoader.class, classes = { IridaApiJdbcDataSourceConfig.class,
		IridaApiPropertyPlaceholderConfig.class })
@TestExecutionListeners({ DependencyInjectionTestExecutionListener.class, DbUnitTestExecutionListener.class })
@ActiveProfiles("it")
@DatabaseSetup("/ca/corefacility/bioinformatics/irida/ria/web/ProjectsPageIT.xml")
@DatabaseTearDown("classpath:/ca/corefacility/bioinformatics/irida/test/integration/TableReset.xml")
public class EditProjectMembersPageIT {
	public static final Long PROJECT_ID = 1L;
	private WebDriver driver;
	private EditProjectMembersPage editMembersPage;

	@Before
	public void setUp() {
		this.driver = new PhantomJSDriver();
		LoginPage loginPage = LoginPage.to(driver);
		loginPage.doLogin();

		editMembersPage = new EditProjectMembersPage(driver, PROJECT_ID);
	}

	@After
	public void destroy() {
		if (driver != null) {
			driver.close();
			driver.quit();
		}
	}

	@Test
	public void testRemoveUser() {
		editMembersPage.clickRemoveUserButton(2l);
		editMembersPage.clickModialPopupButton();
		List<String> userNames = editMembersPage.getUserNames();
		assertEquals(1, userNames.size());
	}
}
